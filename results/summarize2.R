gr <- read.csv('results/grid_results.csv', stringsAsFactors = FALSE)
agg <- aggregate(list(drift = gr$mean_drift, ctx = gr$mean_context_drift, ssr = gr$mean_ssr), list(G = gr$G, pattern = gr$pattern), mean)
agg$shrink_pct <- round(100 * (1 - agg$ctx / agg$drift), 1)
agg <- agg[order(agg$G, agg$pattern), ]
agg$drift <- round(agg$drift, 3)
agg$ctx <- round(agg$ctx, 3)
agg$ssr <- round(agg$ssr, 3)
cat('--- By G x pattern ---\n')
print(agg, row.names = FALSE)
